import java.util.*;
public class main
{
    public static void main(String args[])
    {
        int num, reversednum;
        Scanner in = new Scanner (System.in);
        System.out.println("Enter the number");
        num=in.nextInt();
        num=reversednum;
        if (num==reversednum)
            System.out.println("The number is palindrome");
        else
            System.out.println("The number is not a palindrome ");
    }
}
